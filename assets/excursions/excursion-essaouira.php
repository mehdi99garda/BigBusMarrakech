                        <p class="article">
L'excursion à Essaouira consiste à visiter le paradis atlantique marocain, ville du patrimoine mondial, la ville fortifiée d'Essaouira, ex-Mogador est une œuvre architecturale qui ne se fatigue jamais de contempler. Marcher à travers les petites ruelles de la ville où les vagues de l'Atlantique s'écrasent est sûrement la meilleure façon de prendre le pouls d'Essaouira, en conservant le charme et l'authenticité d'une terre perdue dans le temps.
