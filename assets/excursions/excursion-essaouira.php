Votre excursion à Essaouira vous permet de connaître l'histoire d'une ville cosmopolite qui fut un centre de commerce maritime entre l'Europe et l'Afrique au 18ème siècle. Les fortifications ont survécu à l'épreuve du temps et des peuples, des Phéniciens aux Romains, des Portugais aux Français. Egalement connue sous l'ancien nom portugais de Mogador.
<br /><br />
Pendant cette excursion d’une journée vous allez passez par des villages Berbères et admirant le paysage du désert de nombreux hectares de l’arganier, en a profité pour arrêter de voir les chèvres grimper l'arganier, puis un arrêt pour visiter une coopérative féminine de huile d'Argan, Cette ancienne plante vit exclusivement dans cette région du Maroc, est également protégée par l'UNESCO. De la noix de cette olive est extraite, avec un mortier de pierre, une huile qui recueille une série infinie de propriétés. Dans cette coopérative vous allez prendre une idée du processus d'extraction des huiles d’argan et d'autres produits, et avant d'arriver à Essaouira, un arrêt pour la vue panoramique de la ville d’Essaouira, Une caméra est essentielle pour des belles photos de paysages.
<br /><br />
La visite d'Essaouira peut se faire tranquillement en une matinée jusqu'à l'heure du déjeuner, elle vous donne amplement le temps de visiter ses rues, ses nombreux ateliers artisanales et ses boutiques d’argent. La Médina d'Essauira a été déclarée site du patrimoine mondial par l'Unesco en 2001. Essaouira est une parfaite aquarelle de couleurs, avec un horizon bleu qui contraste avec les fortifications ocres roses et ses maisons blanches avec des cadres bleus.
<br /><br />
Ses magnifiques plages attirent de nombreux touristes, qui recherchent également le soleil, le golf, les sports d'aventure liés au vent, au surf, au windsurf et au kite surf.

